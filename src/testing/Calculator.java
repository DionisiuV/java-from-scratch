package testing;

public class Calculator {
    //Addition
    public int add(int a, int b) {
        return a + b;
    }

    //Subtraction
    public int sub(int a, int b) {
        return a - b;
    }

    //Multiplication
    public int multiply(int a, int b) {
        return a * b;
    }

    //Division
    public int divide(int a, int b) {
        return a / b;
    }
}